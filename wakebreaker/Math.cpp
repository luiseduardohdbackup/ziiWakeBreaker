float sqrtx (float x)
{
	int xhalf = MULX(FTOX(0.5f),FTOX(x));
    
	int i = *(int*)&x;
    
	i = 0x5f3759df - (i >> 1);
    
	x = *(float*)&i;
    
	int xx = FTOX(x);

	xx = MULX(xx,(FTOX(1.5f) - MULX(xhalf,MULX(xx,xx))));
    
	return XTOF( DIVX(ITOX(1),xx) );
}
//------------------------
int sqrtx(int32 x)
{
	float fx = XTOF(x);

	int xhalf = MULX(FTOX(0.5f),x);
    
	int i = *(int*)&fx;
    
	i = 0x5f3759df - (i >> 1);
    
	fx = *(float*)&i;
    
	int xx = FTOX(fx);

	xx = MULX(xx,(FTOX(1.5f) - MULX(xhalf,MULX(xx,xx))));
    
	return  DIVX(ITOX(1),xx);
}
